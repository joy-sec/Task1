package net.apkkothon.tourkit.models;



public class Place {
    String postID,placeName,placeDetails,placeImage,placeDistrict,placeDivision,placeLat,placeLon;


    public String getPlaceLat() {
        return placeLat;
    }

    public void setPlaceLat(String placeLat) {
        this.placeLat = placeLat;
    }

    public String getPlaceLon() {
        return placeLon;
    }

    public void setPlaceLon(String placeLon) {
        this.placeLon = placeLon;
    }

    public String getPlaceDivision() {
        return placeDivision;
    }

    public void setPlaceDivision(String placeDivision) {
        this.placeDivision = placeDivision;
    }

    public String getPostID() {
        return postID;
    }

    public String getPlaceDistrict() {
        return placeDistrict;
    }

    public void setPlaceDistrict(String placeDistrict) {
        this.placeDistrict = placeDistrict;
    }

    public void setPostID(String postID) {
        this.postID = postID;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getPlaceDetails() {
        return placeDetails;
    }

    public void setPlaceDetails(String placeDetails) {
        this.placeDetails = placeDetails;
    }

    public String getPlaceImage() {
        return placeImage;
    }

    public void setPlaceImage(String placeImage) {
        this.placeImage = placeImage;
    }
}
